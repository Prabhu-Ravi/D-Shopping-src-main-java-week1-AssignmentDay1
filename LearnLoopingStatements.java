package week1.day1;

public class LearnLoopingStatements {

	public static void main(String[] args) {
		  //Print from 1 to 100
		//for (intialisation; Condition; Increment / Decrement)
		for(int p=1; p<=100; p++) {
			System.out.println("Welcom to Prabhu");
		}
		
		for (int p=100; p>=0; p--)
			System.out.println(p);
	}

}
